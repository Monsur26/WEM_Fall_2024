package javacontrolflow.nestedforloop;

public class JavaNestedForLoop {
    public static void main(String[] args) {
        for (int i = 1; i <=3 ; i++) {
            for (int j = 4; j <=6 ; j++) {
                System.out.println("i value :"+ i + " corresponding j value "+j);
            }
        }
    }
}
