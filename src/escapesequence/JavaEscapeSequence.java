package escapesequence;

public class JavaEscapeSequence {
    public static void main(String[] args) {
        // this is for inserting  " " inside your string
        System.out.println("My name is \"Monsur\" ");
        // putting a tab
        System.out.println("My name is:\tMonsur ");
        // putting strings on new line
        System.out.println("My home address is :\n123 abc road \nNew York, New York, 11000");
        //backspace
        System.out.println("MY Name\b\b\b\b");
        //backslash
        System.out.println("MY Name\\");
    }
}
